// Helper function to get words for a locale
export function getWordsForLocale(locale: Locale): string[] {
  return WORD_LISTS[locale] ?? WORD_LISTS.en;
}

// Translate an English word to a locale-specific word
export function translateWord(englishWord: string, locale: string): string {
  const englishIndex = ENGLISH_WORDS.indexOf(englishWord);
  if (englishIndex === -1) {
    // Word not found, return original
    return englishWord;
  }

  const translatedWords = WORD_LISTS[locale as Locale] ?? WORD_LISTS.en;
  return translatedWords[englishIndex] ?? englishWord;
}

